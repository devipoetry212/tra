        <h3 class="text-center">Apa itu jurusan DPIB ???</h3>
        <br>
        <p><b>DPIB</b>adalah singkatan dari <b>Desain Pemodelan dan Informasi Bangunan</b> dan merupakan sebuah jurusan yang ada di Sekolah Menengah Kejuruan (SMK).<br><b>DPIB</b> adalah jurusan yang mempelajari tentang perencanaan bangunan, pelaksanaan pembuatan gedung, dan perbaikan gedung. kegiatannya adalah belajar menggambar desain rumah, gedung, dan apartemen, menghitung biaya bangunan, melaksankan pembangunan, serta memelihara konstruksi bangunan.</p>

        <h3 class="text-center">Tujuan jurusan DPIB</h3>
        <p>Tujuan Kompetensi Keahlian Desain Pemodelan dan Informasi Bangunan adalah membekali peserta didik dengan keterampilan, pengetahuan, dan sikap agar kompeten dalam :</p>
        <ul>
            <p>
                <i class="bi bi-1-circle-fill"></i> Melakukan pekerjaan sebagai Drafter/Juru Gambar dalam pekerjaan perencanaan bangunan.
            </p>
            <p>
                <i class="bi bi-2-circle-fill"></i> Melakukan pekerjaan sebagai Drafter/Juru Gambar dalam pekerjaan pelaksanaan bangunan.
            </p>
            <p>
                <i class="bi bi-3-circle-fill"></i> Melakukan pekerjaan jasa penggambaran bangunan secara mandiri / berwirausaha di Studio Gambar.
            </p><br>


