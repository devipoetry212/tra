        <br>
        <p><b>RPL</b>adalah singkatan dari <b>Rekayasa Perangkat Lunak</b> dan merupakan sebuah jurusan yang ada di Sekolah Menengah Kejuruan (SMK).<br><b>RPL</b> adalah sebuah jurusan yang mempelajari dan mendalami semua cara-cara pengembangan perangkat lunak termasuk pembuatan, pemeliharaan, manajemen organisasi pengembangan perangkat lunak dan manajemen kualitas.</p>
